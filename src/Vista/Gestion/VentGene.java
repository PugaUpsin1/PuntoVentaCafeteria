    public double sumaTot(){
        int contar = tblVenGen.getRowCount();
        double Total = 0;
        for(int i = 0; i < contar ; i++){
            Total = Total + Double.parseDouble(tblVenGen.getValueAt(i, 1).toString());
        }
        String tot = Double.toString(Total);
        this.lblDinTot.setText(tot);
        return Total;
    }
    
